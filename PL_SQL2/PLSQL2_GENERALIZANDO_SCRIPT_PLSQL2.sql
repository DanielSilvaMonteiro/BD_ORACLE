--CONCEITO DE LOOP;
DECLARE
    V_SEGMERCADO CLIENTE.SEGMERCADO_ID%TYPE := 3;
    V_ID CLIENTE.ID%TYPE := 1;
    V_NUMCLI INTEGER := 10;
BEGIN
    SELECT COUNT(*) INTO V_NUMCLI FROM CLIENTE;
    LOOP
        ATUALIZAR_SEGMERCADO (V_ID, V_SEGMERCADO);
        V_ID := V_ID +1;
    EXIT WHEN V_ID > V_NUMCLI;
    END LOOP;
END;

COMMIT;

