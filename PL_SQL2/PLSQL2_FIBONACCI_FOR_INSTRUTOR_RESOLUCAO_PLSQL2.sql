DECLARE
    V_VALOR NUMBER;
    V_VALOR_SAIDA NUMBER;
BEGIN
    V_VALOR := 45;
    NUMEROS_FIBONACCI_FOR_INSTRUTOR(V_VALOR, V_VALOR_SAIDA);
   dbms_output.put_line('Resultado: ' || V_VALOR_SAIDA);
END;