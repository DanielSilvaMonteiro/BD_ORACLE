CREATE OR REPLACE TRIGGER TG_TAB_FATURAMENTO
AFTER INSERT OR UPDATE OR DELETE ON ITENS_NOTAS --SERA EXECUTADO DEPOIS DE INSERIR RM ITENS_NOTAS
BEGIN
    DELETE FROM TAB_FATURAMENTO;
    INSERT INTO TAB_FATURAMENTO
    SELECT N.DATA_VENDA,
       SUM(ITN.QUANTIDADE * ITN.PRECO) AS FATURAMENTO
    FROM NOTAS N
    INNER JOIN ITENS_NOTAS ITN
    ON N.NUMERO = ITN.NUMERO
    GROUP BY N.DATA_VENDA;
END;

