SELECT 3.4 FROM DUAL;

-- FUNCAO ROUND ARREDONDA O NUMERO
SELECT ROUND(3.4), ROUND(3.6), ROUND(3.5) FROM DUAL;

-- FUNCAO TRUNC ARREDONDA PARA O NUMERO INTEIRO 
SELECT TRUNC(3.4), TRUNC(3.6), TRUNC(3.5) FROM DUAL;

-- CEIL ARREDONDA SEMPRE UM A MAIS QUE O NUMERO INTEIRO
SELECT CEIL(3.4), CEIL(3.6), CEIL(3.5) FROM DUAL;

-- RETORNA O INTEIRO 
SELECT FLOOR(3.4), FLOOR(3.6), FLOOR(3.5) FROM DUAL;

-- POWER 10*10*10*10 / 34*34*34*32 / 2*2
SELECT POWER(10,4), POWER(34,4), POWER(2,2) FROM DUAL;

--SQRT RAIZ QUADRADA
SELECT SQRT(144), SQRT(9), SQRT(25) FROM DUAL;

--ABS VALOR ABSOLUTO
SELECT ABS(10), ABS(-10), ABS(-22.5) FROM DUAL;

--MOD RESTO
SELECT MOD(10,6), MOD(5,2), MOD(2,2) FROM DUAL;